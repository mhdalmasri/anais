import createStore from 'react-waterfall';

/* @see https://github.com/didierfranc/react-waterfall */
const config = {
    initialState: {
        isMenuVisible: false,
        count: 0,
    },
    actionsCreators: {
        toggleMenu: (previousState, actions, value) => ({
            isMenuVisible: (typeof value === 'boolean') ? value : !previousState.isMenuVisible,
        }),
        increaseCount: (previousState, actions, value) => ({
            count: previousState.count + value,
        }),
    },
};

export const { Provider, connect, actions } = createStore(config);

